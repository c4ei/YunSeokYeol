package protowire

import (
	"github.com/c4ei/c4exd/app/appmessage"
	"github.com/pkg/errors"
)

type converter interface {
	toAppMessage() (appmessage.Message, error)
}

// ToAppMessage converts a C4exdMessage to its appmessage.Message representation
func (x *C4exdMessage) ToAppMessage() (appmessage.Message, error) {
	if x == nil {
		return nil, errors.Wrapf(errorNil, "C4exdMessage is nil")
	}
	converter, ok := x.Payload.(converter)
	if !ok {
		return nil, errors.Errorf("received invalid message")
	}
	appMessage, err := converter.toAppMessage()
	if err != nil {
		return nil, err
	}
	return appMessage, nil
}

// FromAppMessage creates a C4exdMessage from a appmessage.Message
func FromAppMessage(message appmessage.Message) (*C4exdMessage, error) {
	payload, err := toPayload(message)
	if err != nil {
		return nil, err
	}
	return &C4exdMessage{
		Payload: payload,
	}, nil
}

func toPayload(message appmessage.Message) (isC4exdMessage_Payload, error) {
	p2pPayload, err := toP2PPayload(message)
	if err != nil {
		return nil, err
	}
	if p2pPayload != nil {
		return p2pPayload, nil
	}

	rpcPayload, err := toRPCPayload(message)
	if err != nil {
		return nil, err
	}
	if rpcPayload != nil {
		return rpcPayload, nil
	}

	return nil, errors.Errorf("unknown message type %T", message)
}

